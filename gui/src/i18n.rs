use crate::config::Lang;

/// Look up a translation key. Returns the English string if `lang` is En or
/// if no Zh translation is registered for `key`.
pub fn t(lang: Lang, key: &str) -> &'static str {
    if matches!(lang, Lang::En) {
        return en(key);
    }
    let zh_val = zh(key);
    if zh_val.is_empty() {
        en(key)
    } else {
        zh_val
    }
}

